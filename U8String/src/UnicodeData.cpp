UnicodeData::Unicode UnicodeData::lowercase_mapping(Unicode character)
{
	auto it = std::lower_bound(m_lowers.begin(), m_lowers.end(), character, [](const std::pair<Unicode, Unicode>& itPair, Unicode characterIt) {
		return itPair.first < characterIt;
	});

	if (it == m_lowers.end())
		throw std::runtime_error("Character " + std::to_string(character) + " is not handled.");
	else
	{
		if (it->first != character)
			return character;
		else
			return it->second;
	}
}

