UnicodeData::BidirectionalCategory UnicodeData::bidirectional_category(Unicode character)
{
	auto it = std::upper_bound(m_bidirectionals.begin(), m_bidirectionals.end(), character, [](Unicode characterIt, const std::pair<Unicode, BidirectionalCategory>& itPair) {
		return itPair.first > characterIt;
	});

	if (it == m_bidirectionals.end())
		throw std::runtime_error("Character " + std::to_string(character) + " is not handled.");
	else
		return it->second;
}

